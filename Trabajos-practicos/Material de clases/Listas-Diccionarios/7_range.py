# Recorriendo un rango de números
# Utilizamos un bucle for para recorrer un rango de números del 0 al 4.
# La range(5)función genera una secuencia de números que comienza en 0 y termina en 4 (exclusivo).
# La variable de bucle i toma cada valor del rango y la print(i)declaración imprime el valor de i durante
# cada iteración. Como resultado, los números 0, 1, 2, 3 y 4 se imprimirán en líneas separadas.

for i in range(5):
    print(i)

for i in range(4, 20, 2):
    print(i)
