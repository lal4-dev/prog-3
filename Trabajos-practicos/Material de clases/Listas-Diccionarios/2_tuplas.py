# Una tupla en Python es una estructura de datos similar a una lista, pero con la diferencia clave de que es inmutable,
# lo que significa que una vez creada, no se pueden cambiar, añadir ni eliminar sus elementos. Se utilizan cuando
# necesitas una secuencia de elementos que no se deben modificar.
# Las tuplas se definen utilizando paréntesis () y pueden contener diferentes tipos de datos, al igual que las listas.


# Tupla de nombres
