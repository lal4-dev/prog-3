# Colecciones de datos Listas: Las listas en Python son un tipo de estructura de datos que permite almacenar múltiples
# elementos en una única variable. Son colecciones ordenadas y mutables, lo que significa que puedes modificar sus
# elementos después de crearlas (añadir, eliminar o cambiar elementos). Las listas se definen entre corchetes [] y
# los elementos dentro de una lista pueden ser de cualquier tipo: números, cadenas de texto, otros objetos,
# e incluso otras listas.


